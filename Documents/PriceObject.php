namespace ONGR\MagentoConnectorBundle\Documents;

use ONGR\ElasticsearchBundle\Annotation as ES;

/**
 * PriceObject document.
 *
 * @ES\Object
 */
class PriceObject
{
    /**
     * @var float
     */
    private $price;

    /**
     * @return mixed
     */
    public function getPrice()
    {
        return $this->price;
    }

    /**
     * @param mixed $price
     */
    public function setPrice($price)
    {
        $this->price = $price;
    }
}
